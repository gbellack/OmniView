/* REQUIRES: the x, y, and color.
 * EFFECTS: Sets a pixel.
 */
void DrawPixel(int16_t x, int16_t y, uint16_t color) {

	/* Input validation */
	if ((x < 0) || (x >= SSD1306_LCDWIDTH) || (y < 0) || (y >= SSD1306_LCDHEIGHT))
		return;

	// x is which column
	switch (color)
	{
		case WHITE:
			buffer[x+ (y/8)*SSD1306_LCDWIDTH] |=  (1 << (y&7));
			break;
		case BLACK:
			buffer[x+ (y/8)*SSD1306_LCDWIDTH] &= ~(1 << (y&7));
			break;
		case INVERSE:
			buffer[x+ (y/8)*SSD1306_LCDWIDTH] ^=  (1 << (y&7));
			break;
	}
}
